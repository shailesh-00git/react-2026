/*

&& -> and
|| -> reverse
! -> reverse

*/

// and
const loggeedIn = true;
const isPaid = true;
console.log(loggeedIn && isPaid); // true i.e. both are true

// or
const isStudent = true;
const hasNId = false;
console.log(isStudent || hasNId); // give true if any one is true

// reverse
const isSleeping = true;
console.log(!isSleeping); // converts true into false (reverse)
